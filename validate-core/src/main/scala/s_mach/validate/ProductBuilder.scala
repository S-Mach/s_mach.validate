package s_mach.validate

import scala.language.higherKinds

trait ProductBuilder[T[_],A] {
  def field[B](
    name: String,
    unapply: A => B
  )(
    f: T[B] => T[B] = { vb:T[B] => vb }
  )(implicit
    vb: T[B]
  ) : ProductBuilder[T,A]

  def build() : T[A]
}
